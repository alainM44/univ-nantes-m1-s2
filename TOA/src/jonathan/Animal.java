public class Animal {
    private String nom;
    private int age;

    public Animal() {
    }
    
    public Animal(String unNom, int unAge) {
        nom = unNom;
        age = unAge;
    }

    /**
     * @return the nom
     */
    public String getNom() {
        return nom;
    }

    /**
     * @param nom the nom to set
     */
    public void setNom(String nom) {
        this.nom = nom;
    }

    /**
     * @return the age
     */
    public int getAge() {
        return age;
    }

    /**
     * @param age the age to set
     */
    public void setAge(int age) {
        this.age = age;
    }

    /**
     * @return the age
     */
   
    
}
